public class swap_with_third_var {

    public static void main(String[] args) {
        
        int a = 30;
        int b = 20;
        int temp;

        System.out.println("before swaping\n a = "+a +" "+ " b = "+b +"\n");

        temp = a;
        a = b;
        b = temp;

        System.out.println("after swaping\n a = "+a +" "+ " b = "+b);


    }

}
